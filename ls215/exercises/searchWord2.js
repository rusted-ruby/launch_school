function searchWord1(term, text) {
  let newText = text.split(' ').map((word) => {
    if (term.toLowerCase() === word.toLowerCase()) {
      return "**" + word.toUpperCase() + "**"
    } else {
      return word
    }
  }).join(' ')
  return newText
}

function searchWord(term, text){
  let regex = new RegExp(term, 'gi');
  return text.replace(regex, `**${term.toUpperCase()}**`)
}

